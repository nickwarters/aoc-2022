function solve(input: string): any {

    let currentCycle = 0
    let spriteS = 0 
    let spriteE = 2 
    let cnt = 1
    const isVisible = () => spriteS <= currentCycle - 1 && spriteE >= currentCycle - 1

    let output = ''

    input.split('\n').forEach(line => {
        if(line === '') return
        
        cnt = line !== 'noop' ? 2 : 1 

        for(let i = 0; i < cnt; i++){
            currentCycle +=  currentCycle === 40 ? -39: 1
            output = `${output}${currentCycle === 1 ? '\n': ''}${isVisible() ? '#' : '.'}`
        }
        
        if(line === 'noop') return 

        const val = parseInt(line.split(' ')[1]) 
        spriteS = spriteS += val 
        spriteE = spriteE += val 

    })

    return output
}
